	static void HSVtoRGB(double H, double S, double V, double &R, double &G, double &B)
	{
		if (S == 0)
		{
			R = V;
			G = V;
			B = V;
		}
		else
		{
			double var_h = H * 6.0;

			if (var_h == 6.0)
				var_h = 0.0;     // H must be < 1

			int var_i = static_cast<int>(var_h);
			double var_1 = V * (1.0 - S);
			double var_2 = V * (1.0 - S * (var_h - var_i ));
			double var_3 = V * (1.0 - S * (1.0 - (var_h - var_i)));

			double var_r, var_g, var_b;

			if (var_i == 0) 
			{ 
				var_r = V; 
				var_g = var_3; 
				var_b = var_1;
			}
			else if (var_i == 1) 
			{
				var_r = var_2;
				var_g = V; 
				var_b = var_1;
			}
			else if (var_i == 2) 
			{ 
				var_r = var_1;
				var_g = V;
				var_b = var_3;
			}
			else if (var_i == 3)
			{ 
				var_r = var_1;
				var_g = var_2;
				var_b = V;
			}
			else if (var_i == 4)
			{
				var_r = var_3;
				var_g = var_1;
				var_b = V;
			}
			else
			{
				var_r = V;
				var_g = var_1;
				var_b = var_2;
			}

			R = var_r;
			G = var_g;
			B = var_b;
		}

	}

